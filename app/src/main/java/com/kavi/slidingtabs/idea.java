public class idea {

    String ideaId;
    String idealine;

    public idea() {

    }

    public idea(String ideaId, String idealine) {
        this.ideaId = ideaId;
        this.idealine = idealine;
    }

    public String getIdeaId() {
        return ideaId;
    }

    public String getIdealine() {
        return idealine;
    }
}
