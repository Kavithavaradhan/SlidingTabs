public class nonacademicupdate {

    String nonacId;
    String nonacline;

    public nonacademicupdate() {

    }

    public nonacademicupdate(String nonacId, String nonacline) {
        this.nonacId = nonacId;
        this.nonacline = nonacline;
    }

    public String getNonacId() {
        return nonacId;
    }

    public String getNonacline() {
        return nonacline;
    }
}
