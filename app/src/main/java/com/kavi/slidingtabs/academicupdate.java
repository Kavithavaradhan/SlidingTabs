public class academicupdate {
    String acaId;
    String acaline;

    public academicupdate() {
    }

    public academicupdate(String acaId, String acaline) {
        this.acaId = acaId;
        this.acaline = acaline;
    }

    public String getAcaId() {
        return acaId;
    }

    public String getAcaline() {
        return acaline;
    }
}
